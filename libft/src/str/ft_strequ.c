#include <libft.h>

int		ft_strequ(char const *src1, char const *src2)
{
	CHK(src1 == 0 || src2 == 0, 0);
	while (*src1 == *src2)
	{
		if (*src1 == '\0' && *src2 == '\0')
			return (1);
		src1++;
		src2++;
	}
	return (0);
}
